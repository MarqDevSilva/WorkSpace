    public void initModel(){
        ticketModel = new DefaultListModel();
    }
    
    public void loadTK(Date date){
        List<Ticket> listTK = TK.getDate(date);
        for(int i = 0; i < listTK.size(); i++){
            Ticket ticket = listTK.get(i);
            ticketModel.add(i, ticket);
        }
        jList1.setModel(ticketModel);
    }
           
