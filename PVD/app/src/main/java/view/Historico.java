    private void CampoDataKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_CampoDataKeyPressed
        // TODO add your handling code here:
         if(evt.getKeyCode()==KeyEvent.VK_ENTER && !"".equals(CampoData.getText())){
             String str = CampoData.getText();
             SimpleDateFormat sdf = new SimpleDateFormat ("dd/MM/yyyy");
             try {
                 Date date = new java.sql.Date(sdf.parse(str).getTime());
                 TK.getDate(date);
                 loadTK(date);
             } catch (ParseException ex) {
                 Logger.getLogger(Historico.class.getName()).log(Level.SEVERE, null, ex);
             }
         } 
    }//GEN-LAST:event_CampoDataKeyPressed

